package CoreJavaPrograms.PractisePrograms.source.lab1;
public class Program4
{
    public static void main(String[] args)
    {
       
        int a=1;
        while(a<10)
        {
            System.out.println(a);
            a++;

        } 
        for(int i=1;i<=10;i++)
        {
            System.out.println(i);
        }
        int b=1;

        do
        {
            System.out.println(b);
            b++;

        }while(b<=10);
    }
}